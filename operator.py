# Sample Code: Operators in Action
a = 10
b = 3

# Arithmetic
print(f"Addition (a + b): {a + b}")       # 13
print(f"Division (a / b): {a / b}")       # 3.33...
print(f"Modulo (a % b): {a % b}")         # 1

# Comparison
print(f"Equal (a == b): {a == b}")       # False
print(f"Greater than (a > b): {a > b}")   # True

# Logical
x = True
y = False
print(f"AND (x and y): {x and y}")     # False
print(f"NOT (not x): {not x}")         # False