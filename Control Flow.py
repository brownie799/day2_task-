# Sample Code: Loops and Conditionals
score = 75
if score >= 90:
    print("Grade A")
elif score >= 70:
    print("Grade C")
else:
    print("Grade F")

print("\nFor loop:")
for i in range(3):
    print(i)

print("\nWhile loop:")
count = 0
while count < 2:
    print(count)
    count += 1